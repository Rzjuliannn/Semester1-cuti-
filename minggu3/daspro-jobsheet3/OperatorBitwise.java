public class OperatorBitwise {
    
    public static void main(String[] args) {
        int a = 8; // 0101 (angka biner)
        int b = 11; // 0111

        // bitwise OR(|)
            System.out.println(a|b);

        //bitwise AND(&)
            System.out.println(a&b);

        //bitwise XOR(^)
            System.out.println(a^b);

        //bitwise complement(~)
            System.out.println(~a);
    }
}
