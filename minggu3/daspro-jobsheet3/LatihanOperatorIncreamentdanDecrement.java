public class LatihanOperatorIncreamentdanDecrement {
    
    public static void main(String[] args) {
        int i = 2;

//increament
    System.out.println("i : " + i);
    System.out.println("++i : " + ++i);
    System.out.println("i++ : " + i++);

//decreament
    System.out.println("--i : " + --i);
    System.out.println("i-- : " + i--);
    System.out.println("i : " + i);
    
    
    }
}
